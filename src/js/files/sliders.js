			observer: true,
			observeParents: true,
			slidesPerGroup: 1,
			slidesPerView: 1,
			spaceBetween: 0,
			speed: 800,
			
			// Пагинация
			pagination: {
				el: '.advantages__pagination',
				type: 'custom',
				renderCustom: function (swiper, current, total) {
					return '<span>' + (current < 10 ? '0' : '') + current + "</span>";
				},
			},

			// Кнопки "влево/вправо"
